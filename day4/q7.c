#include<stdio.h>
  
int main() {
 
    int a,b;
 
    printf("enter first number(0-9): ");
    scanf("%d",&a);
   
    printf("enter second (0-9): ");
    scanf("%d",&b);
 
    printf(" number before swap = %d %d\n", a,b);
 
    printf(" number after swap = %d %d\n", b,a);
  
 
    return 0;
}
  

